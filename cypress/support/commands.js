Cypress.Commands.add("login", (login, password) => {
    cy.get("#kc-form-login").within(() => {
        cy.get("input[tabindex=1]").type(login)
        cy.get("input[tabindex=2]").type(password)
        cy.get("input[tabindex=4]").click()
    })
})

